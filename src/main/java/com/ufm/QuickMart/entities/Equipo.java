package com.ufm.QuickMart.entities;

import org.springframework.data.annotation.Id;
import org.springframework.data.mongodb.core.mapping.Document;
import org.springframework.data.mongodb.core.mapping.DBRef;

@Document(collection = "equipo")  // Nombre de la colección en MongoDB
public class Equipo {

    @Id
    private String id;  // Cambia a String para compatibilidad con MongoDB

    private String nombre;

    @DBRef  // Para establecer una referencia a otra colección, como Torneo
    private Torneo torneo;

    // Getters y Setters
    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public Torneo getTorneo() {
        return torneo;
    }

    public void setTorneo(Torneo torneo) {
        this.torneo = torneo;
    }
}
